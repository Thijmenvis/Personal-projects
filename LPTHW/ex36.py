# homework game

# 1. on a sheet of paper or an index card, write a list of tasks you need to
#   complete the game. This is your todo list.
# 2. pick the easiest thing you can do from your list.
# 3. write out english comments in your source file as a guide for how you would
#   accomplish this task in your code
# 4. write some of the code under the english comments
# 5. quickly run your script to see if that code worked.
# 6. keep working in a cycle of writing some code, running it to test it,
#   and fixing it until it works.
# 7. cross this task off your list, then pick your next easiest task and repeat.
